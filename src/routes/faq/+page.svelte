<p class="subduv">Quais são os Objetivos do Site?</p>
<p class="textoduv">
	Esse site foi feito com o objetivo inicial de auxiliar no processo empregatício através de um
	site, e com isso facilitar as negociações e contratações feitas entre um prestador de serviço e o
	cliente. O mercado de trabalho a cada dia se mostra mais acirrado e competitivo, sendo assim vimos
	a necessidade de expandir as formas de ingresso em diversos ramos, sejam eles meios de trabalho
	remoto, online e fisicamente. No site ficarão disponíveis os dados de contato do trabalhador bem
	como suas experiências anteriores na área, caberá ao interessado avaliar os prestadores
	disponíveis de acordo com a sua necessidade, e entrar em contato diretamente com o prestador caso
	deseje contratar um serviço. No site tanto o cliente quanto o prestador terão cadastros e algumas
	descrições caso queiram sobre si e sobre os trabalhos feitos, dessa forma é possível promover um
	ambiente de maior imparcialidade e segurança em ambos os lados da negociação.
</p>

<p class="subduv">Quais dados são visíveis do meu currículo?</p>
<p class="textoduv">
	É disponibilizado somente seu nome, profissões, foto de perfil, sua descrição e sua avaliação
	média. Estes dados são disponibilizados para que contas falsas ou sem dados suficientes não acabem
	atrapalhando na seleção de outros trabalhadores trabalhadores.
</p>

<p class="subduv">O que eu faço se não tiver uma opção para minha profissão?</p>
<p class="textoduv">
	ReadyWorker tentou colocar o máximo de profissões possíveis para minimizar esses casos, mas temos
	ciencia de que isso pode acontecer, então nesses casos você pode escolher a opção mais similar e
	colocar em sua descrição exatamente o que você faz.
</p>

<p class="subduv">Onde posso achar os termos de uso do site?</p>
<p class="textoduv">
	No fim de cada página tem alguns links, onde um deles iniciará um download do arquivo de termos de
	uso do ReadyWorker.
</p>
<p class="subduv">Origem do nome "ReadyWorker":</p>
<p class="textoduv">
	O nome foi criado de forma espontânea, mas que se encaixou como uma luva, já que Ready em
	português significa "Pronto" e Worker significa "Trabalhador", formando assim TrabalhadorPronto, o
	que acaba fazendo referência à usabilidade do site, já que temos um banco de profissionais prontos
	para atender seus requerimentos.
</p>
<p class="subduv">Origem da Logo:</p>
<p class="textoduv">
	A logo não tem muito mistério, já que o R e o W são das palavras ReadyWorker, enquanto que a
	engrenagem significa o papel que o site exerce, sendo uma engrenagem para muitos profissionais
	consigam ganhar mais experiência de trabalho
</p>
