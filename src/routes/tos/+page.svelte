	<h3>DE CONSENTIMENTO PARA TRATAMENTO DE DADOS PESSOAIS DE CANDIDATO.</h3>
	<br />
	Este documento visa registrar a manifestação livre, informada e inequívoca pela qual o CANDIDATO concorrendo
	à vaga de emprego disponibilizada pelo portal Readyworker.COM.BR (“Readyworker”) concorda com o tratamento
	de seus dados pessoais para finalidade específica, em conformidade com a Lei nº 13.709 (Lei Geral de
	Proteção de Dados Pessoais – LGPD).
	<br />
	Ao assinar o presente termo, o CANDIDATO consente e concorda que o Readyworker tome decisões referentes
	ao tratamento de seus dados pessoais, bem como realize o tratamento destes, envolvendo operações como
	a coleta, produção, recepção, classificação, utilização, acesso, reprodução, transmissão, distribuição,
	processamento, arquivamento, armazenamento, eliminação, avaliação ou controle da informação, modificação,
	comunicação, transferência, difusão ou extração.
	<br />
	Este termo está vinculado ao AVISO LEGAL e ao CONTRATO DE PRESTAÇÃO DE SERVIÇOS do Readyworker, que
	se encontra disponível para acesso no website (https://Readyworker.com.br).
	<br />
	<br />
	<h3>DOS DADOS PESSOAIS</h3>
	Os dados pessoais coletados do CANDIDATO são:<br />
	1. nome completo;<br />
	2. data de nascimento;<br />
	3. endereço residencial;<br />
	4. número de telefone e contato WhatsApp;<br />
	5. endereço de e-mail pessoal;<br />
	6. documentos de identificação como RG, CPF e CNH;<br />
	7. currículo;<br />
	8. certificado de conclusão de curso ou declaração de matrícula.<br />
	<br />
	O tratamento dos dados pessoais poderá ser realizado para possibilitar o processo seletivo da vaga,
	possível estabelecimento de vínculo contratual e todos os atos que decorram da eventual relação de
	trabalho a ser firmada entre o CANDIDATO e as empresas.
	<br /> DO COMPARTILHAMENTO E SEGURANÇA DOS DADOS O Readyworker fica desde já autorizado a
	compartilhar os dados pessoais do CANDIDATO com empresas, bem como sua área interna de gestão de
	pessoas, com o gestor da área que deu origem à vaga, com empresas de recrutamento e seleção, com
	redes sociais de negócios e com empresas terceiras que fornecem licença de software para
	armazenamento e gestão de dados.
	<br />
	O Readyworker responsabiliza-se pela manutenção de medidas de segurança, técnicas e administrativas
	aptas a proteger os dados pessoais de acessos não autorizados e de situações acidentais ou ilícitas
	de destruição, perda, alteração, comunicação ou qualquer forma de tratamento inadequado ou ilícito.
	<br />
	Em conformidade ao artigo 48 da Lei nº 13.709, o Readyworker comunicará ao CANDIDATO e a empresa, bem
	como à Autoridade Nacional de Proteção de Dados – ANPD a ocorrência de incidente de segurança que possa
	acarretar risco ou dano relevante ao CANDIDATO.
	<br />
	<br />
	<h3>DO TÉRMINO DO TRATAMENTO DOS DADOS</h3>
	O Readyworker poderá manter e tratar os dados pessoais do CANDIDATO durante todo o período em que os
	mesmos forem pertinentes ao alcance das finalidades listadas neste termo ou em razão de obrigação legal.
	<br />
	O CANDIDATO poderá solicitar ao Readyworker, a qualquer momento, que sejam eliminados seus dados pessoais
	por meio do e-mail Readyworker@gmail.com, sendo que sua solicitação será considerada de acordo com
	as leis aplicáveis.
	<br />
	DOS DIREITOS DO CANDIDATO
	<br />
	O CANDIDATO tem direito a obter do Readyworker, em relação aos dados por ele tratados, a qualquer momento
	e mediante requisição:
	<br />
	• confirmação da existência de tratamento;<br />
	• acesso aos dados;<br />
	• correção de dados incompletos, inexatos ou desatualizados;<br />
	• anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade
	com o disposto na Lei nº 13.709;<br />
	• portabilidade dos dados, mediante requisição expressa;<br />
	• eliminação dos dados pessoais tratados com o consentimento do CANDIDATO, exceto nas hipóteses previstas
	no artigo 16 da Lei nº 13.709;<br />
	• informação sobre a possibilidade de não fornecer consentimento e sobre as consequências da negativa;<br
	/>
	• revogação do consentimento, nos termos do § 5º do artigo 8º da Lei nº 13.709.<br />
	<br />
	<h3>DIREITO DE REVOGAÇÃO DO CONSENTIMENTO</h3>
	Conforme disposto acima, este consentimento poderá ser revogado pelo CANDIDATO, a qualquer momento,
	mediante solicitação ao Readyworker.
